DROP VIEW IF EXISTS OrdiniPerCategoria;

DROP TABLE IF EXISTS Prodotto;
DROP TABLE IF EXISTS StrumentoACorda;
DROP TABLE IF EXISTS StrumentoAFiato;
DROP TABLE IF EXISTS StrumentoAPercussione;
DROP TABLE IF EXISTS StrumentoDigitale;
DROP TABLE IF EXISTS Cliente;
DROP TABLE IF EXISTS Ordine;
DROP TABLE IF EXISTS DettaglioOrdine;
DROP TABLE IF EXISTS ClienteBusiness;
DROP TABLE IF EXISTS ClientePrivato;
DROP TABLE IF EXISTS Recensione;
DROP TABLE IF EXISTS Fornitore;
DROP TABLE IF EXISTS DettaglioApprovvigionamento;
DROP TABLE IF EXISTS Approvvigionamento;
DROP TABLE IF EXISTS Consegna;

CREATE TABLE IF NOT EXISTS Prodotto (
    CodiceSKU VARCHAR(20) PRIMARY KEY,
    Nome VARCHAR(100) NOT NULL,
    Prezzo DECIMAL(10,2) NOT NULL,
    QuantitàInMagazzino INTEGER NOT NULL DEFAULT 0,
    DataInserimento DATE NOT NULL,
    Marca VARCHAR(50) NOT NULL,
    PaeseProduzione VARCHAR(50) NOT NULL
);

CREATE TABLE IF NOT EXISTS StrumentoACorda (
    Prodotto VARCHAR(20) PRIMARY KEY,
    TipoStrumento VARCHAR(50) NOT NULL,
    MaterialeCorpo VARCHAR(50) NOT NULL,
    NumeroCorde INTEGER NOT NULL,
    Scala DECIMAL(5,2) NOT NULL,
    -- Attributo per strumenti a corda acustici
    MaterialeCorde VARCHAR(50),
    -- Attributo per strumenti a corda elettrici
    TipoPickup VARCHAR(30),
    FOREIGN KEY (Prodotto) REFERENCES Prodotto(CodiceSKU)
);

CREATE TABLE IF NOT EXISTS StrumentoAFiato (
    Prodotto VARCHAR(20) PRIMARY KEY,
    TipoStrumento VARCHAR(50) NOT NULL,
    MaterialePrincipale VARCHAR(50) NOT NULL,
    Chiave VARCHAR(20) NOT NULL,
    TipoImboccatura VARCHAR(100) NOT NULL,
    FOREIGN KEY (Prodotto) REFERENCES Prodotto(CodiceSKU)
);

CREATE TABLE IF NOT EXISTS StrumentoAPercussione (
    Prodotto VARCHAR(20) PRIMARY KEY,
    TipoStrumento VARCHAR(50) NOT NULL,
    MaterialePrincipale VARCHAR(50) NOT NULL,
    NumeroFusti INTEGER,
    -- Attributo per strumenti a percussione acustici
    TipoPelle VARCHAR(30),
    -- Attributo per strumenti a percussione elettronici
    NumeroSuoni INTEGER,
    FOREIGN KEY (Prodotto) REFERENCES Prodotto(CodiceSKU)
);

CREATE TABLE IF NOT EXISTS StrumentoDigitale (
    Prodotto VARCHAR(20) PRIMARY KEY,
    TipoStrumento VARCHAR(50) NOT NULL,
    NumeroTasti INTEGER NOT NULL,
    Connettività VARCHAR(100) NOT NULL,
    RequisitiAlimentazione VARCHAR(100) NOT NULL,
    FOREIGN KEY (Prodotto) REFERENCES Prodotto(CodiceSKU)
);

CREATE TABLE IF NOT EXISTS Cliente (
    ID VARCHAR(20) PRIMARY KEY,
    Email VARCHAR(100) UNIQUE NOT NULL,
    Password VARCHAR(100) NOT NULL,
    Indirizzo VARCHAR(255) UNIQUE NOT NULL,
    Telefono VARCHAR(20) UNIQUE NOT NULL,
    DataRegistrazione DATE NOT NULL
);

CREATE TABLE IF NOT EXISTS Ordine (
    NumeroOrdine INTEGER PRIMARY KEY,
	Cliente VARCHAR(20) NOT NULL,
    Data DATE NOT NULL,
    Stato VARCHAR(50) NOT NULL DEFAULT 'In elaborazione',
    TotaleOrdine DECIMAL(10,2) NOT NULL,
    MetodoPagamento VARCHAR(50) NOT NULL,
	FOREIGN KEY (Cliente) REFERENCES Cliente(ID),
    CHECK (Stato IN ('In elaborazione', 'Spedito', 'Consegnato', 'Annullato'))
);

CREATE TABLE IF NOT EXISTS DettaglioOrdine (
    Ordine INTEGER,
    Prodotto VARCHAR(20),
    Quantità INTEGER NOT NULL DEFAULT 1,
    FOREIGN KEY (Ordine) REFERENCES Ordine(NumeroOrdine),
    FOREIGN KEY (Prodotto) REFERENCES Prodotto(CodiceSKU),
    CHECK (Quantità > 0)
);

CREATE TABLE IF NOT EXISTS ClienteBusiness (
    Cliente VARCHAR(20) PRIMARY KEY,
    PartitaIVA VARCHAR(20) UNIQUE NOT NULL,
    RagioneSociale VARCHAR(100) NOT NULL,
    IndirizzoSedeLegale VARCHAR(255) UNIQUE NOT NULL,
    FOREIGN KEY (Cliente) REFERENCES Cliente(ID)
);

CREATE TABLE IF NOT EXISTS ClientePrivato (
    Cliente VARCHAR(20) PRIMARY KEY,
    Nome VARCHAR(50) NOT NULL,
    Cognome VARCHAR(50) NOT NULL,
    FOREIGN KEY (Cliente) REFERENCES Cliente(ID)
);

CREATE TABLE IF NOT EXISTS Recensione (
    IDCliente VARCHAR(20),
    Prodotto VARCHAR(20),
    Punteggio INTEGER NOT NULL,
    Commento TEXT NOT NULL,
    Titolo VARCHAR(100) NOT NULL,
    Data DATE NOT NULL,
    PRIMARY KEY (Prodotto, IDCliente, Data),
    FOREIGN KEY (Prodotto) REFERENCES Prodotto(CodiceSKU),
    FOREIGN KEY (IDCliente) REFERENCES Cliente(ID),
    CHECK (Punteggio BETWEEN 1 AND 5)
);

CREATE TABLE IF NOT EXISTS Fornitore (
    PartitaIVA VARCHAR(20) PRIMARY KEY,
    RagioneSociale VARCHAR(100) NOT NULL,
    Nome VARCHAR(50) NOT NULL,
    Indirizzo VARCHAR(255) UNIQUE NOT NULL,
    Email VARCHAR(100) NOT NULL,
    Telefono VARCHAR(20) NOT NULL,
    PersonaContatto VARCHAR(100) NOT NULL
);

CREATE TABLE IF NOT EXISTS DettaglioApprovvigionamento (
    Prodotto VARCHAR(20),
    Fornitore VARCHAR(20) ,
    QuantitàOrdinata INTEGER NOT NULL,
    QuantitàRicevuta INTEGER NOT NULL DEFAULT 0,
    FOREIGN KEY (Prodotto) REFERENCES Prodotto(CodiceSKU),
    FOREIGN KEY (Fornitore) REFERENCES Fornitore(PartitaIVA), 
    CHECK (QuantitàOrdinata > 0),
    CHECK (QuantitàRicevuta >= 0)
);

CREATE TABLE IF NOT EXISTS Approvvigionamento (
    Fornitore VARCHAR(20) ,
    Prodotto VARCHAR(20),
    DataOrdine DATE NOT NULL,
    DataConsegna DATE NOT NULL,
    Stato VARCHAR(30) NOT NULL DEFAULT 'Ordinato',
    PRIMARY KEY (Fornitore, DataOrdine, Prodotto),
    FOREIGN KEY (Fornitore) REFERENCES Fornitore(PartitaIVA),
    FOREIGN KEY (Prodotto) REFERENCES Prodotto(CodiceSKU),
    CHECK (Stato IN ('Ordinato', 'Ricevuto', 'Parzialmente ricevuto', 'Annullato'))
);

CREATE TABLE IF NOT EXISTS Consegna (
    Fornitore VARCHAR(20) ,
    Prodotto VARCHAR(20),
    DataOrdine DATE NOT NULL,
    FOREIGN KEY (Fornitore) REFERENCES Fornitore(PartitaIVA),
    FOREIGN KEY (Prodotto) REFERENCES Prodotto(CodiceSKU),
    FOREIGN KEY DataOrdine REFERENCES Approvvigionamento(DataOrdine)
);

